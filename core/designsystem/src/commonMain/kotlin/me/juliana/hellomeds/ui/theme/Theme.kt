/**
 * Platform-specific color scheme resolution.
 * Selects the appropriate color scheme based on dark mode, dynamic color, and contrast level.
 */
@Composable
expect fun resolveColorScheme(darkTheme: Boolean, dynamicColor: Boolean, contrastLevel: ContrastLevel): ColorScheme

/**
 * HelloMeds Material 3 theme with medical-appropriate rose/pink color palette.
 *
 * Features:
 * - Dynamic color support on Android 12+ (adapts to system wallpaper)
 * - User-configurable dynamic color preference via Appearance settings
 * - Comprehensive light and dark mode color schemes
 * - Medium and high contrast variants activated by system accessibility settings
 * - Complete Material 3 semantic color roles (primary, secondary, tertiary, error, surface variants, etc.)
 *
 * @param darkTheme Whether to use dark theme colors (defaults to system preference)
 * @param dynamicColor Whether to use dynamic colors on supported platforms (Android 12+)
 * @param contrastLevel Contrast level from system accessibility settings (auto-detected by default)
 * @param content The composable content to apply the theme to
 */
@Composable
fun HelloMedsTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = false,
    contrastLevel: ContrastLevel = systemContrastLevel(),
    content: @Composable () -> Unit,
) {
    val colorScheme = resolveColorScheme(darkTheme, dynamicColor, contrastLevel)

    CompositionLocalProvider(LocalContrastLevel provides contrastLevel) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = Typography,
            content = content,
        )
    }
}
